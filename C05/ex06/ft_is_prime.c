#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	j;

	j = 2;
	if (nb <= 1)
		return (0);
	while (j <= (nb / j))
	{
		if (!(nb % j))
			return (0);
		j++;
	}
	return (1);
}
